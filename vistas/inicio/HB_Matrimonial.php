  <h1>HABITACIONES MATRIMONIALES</h1>
  <p>Aquí podrá encontrar las diferentes habitaciones matrimoniales con las que contamos en Hotel Naranjo</p>
  <section>
    <p>
      El Hotel Naranjo cuenta con tres tipos de habitaciones para suplir con las necesidades de nuestros huespdes, de la mejor manera,
      estas son las habitaciones individuales, que asu vez cuentan con una cama de una plaza, tambien estan las habitaciones dobles,
      las cuales estan amobladas con dos camas de una plaza cada una, idelaes para compartir la habitacion con un amigos, para los hijos, etc;
      y por ultimo estan las habitaciones matrimoniales, amobladas con una cama de dos plazas, lo sificientemente comoda para disfrutar de una noche
      junto agradable en la cuidad de chachappoyas, ádemas de lo ya mecionado todas las habitaciones cuentan con baños propios, servicios de Wi-Fi,
      V, agua caliente, y demas.
    </p>
